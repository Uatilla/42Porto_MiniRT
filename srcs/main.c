#include "../includes/minirt.h"

int	main(int argc, char **argv)
{
	t_minirt	data;

	ft_memset(&data, 0, sizeof(data));
	chk_input(&data, argc, argv[1]);
	set_scene(&data, argv[1]);
	start_mlx(&data.canvas);
	render(&data);
}
